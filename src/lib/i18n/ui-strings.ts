export type UiStrings = (typeof uiStrings)["en"];

export function mergeSiteContent<T>(base: T, lang: Lang): T & UiStrings {
  return { ...base, ...uiStrings[lang] } as T & UiStrings;
}

export function formatMsg(template: string, vars: Record<string, string>) {
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, v), template);
}
